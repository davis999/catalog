package io.reactivesw.catelog.domain;

import java.io.Serializable;
import java.util.Date;
import java.util.Objects;
import java.util.Set;

import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.Size;

/**
 * this is entity class for template.
 * 
 * @author Davis
 *
 */
@Entity
@Table(name = "sw_template")
public class Template implements Serializable {

  /**
   * serialVersionUID.
   */
  private static final long serialVersionUID = -366232774085816005L;

  /**
   * id for template. auto generated by database.
   */
  @Id
  @Column(name = "template_id")
  @GeneratedValue(strategy = GenerationType.AUTO)
  private long id;

  /**
   * name for template.
   */
  @Size(max = 64)
  @Column(name = "name", length = 64, nullable = false)
  private String name;

  /**
   * description for template.
   */
  @Size(max = 1024)
  @Column(name = "description", length = 1024)
  private String description;

  /**
   * attributes for template.
   */
  @OneToMany(cascade = CascadeType.ALL, fetch = FetchType.EAGER)
  @JoinTable(name = "sw_template_attribute", joinColumns = @JoinColumn(name = "template_id") ,
      inverseJoinColumns = @JoinColumn(name = "attribute_id") )
  private Set<Attribute> attributes;

  /**
   * create time for template.
   */
  @Column(name = "create_time")
  private Date createTime;

  /**
   * last modified time for template.
   */
  @Column(name = "last_modified_time")
  private Date lastModifiedTime;

  /**
   * products for template.
   */
  @OneToMany(cascade = CascadeType.ALL, fetch = FetchType.LAZY)
  private Set<Product> products;

  /**
   * get id.
   * 
   * @return the id
   */
  public long getId() {
    return id;
  }

  /**
   * set id.
   * 
   * @param id the id to set
   */
  public void setId(long id) {
    this.id = id;
  }

  /**
   * get name.
   * 
   * @return the name
   */
  public String getName() {
    return name;
  }

  /**
   * set name.
   * 
   * @param name the name to set
   */
  public void setName(String name) {
    this.name = name;
  }

  /**
   * get description.
   * 
   * @return the description
   */
  public String getDescription() {
    return description;
  }

  /**
   * set description.
   * 
   * @param description the description to set
   */
  public void setDescription(String description) {
    this.description = description;
  }

  /**
   * get attributes.
   * 
   * @return the attributes
   */
  public Set<Attribute> getAttributes() {
    return attributes;
  }

  /**
   * set attributes.
   * 
   * @param attributes the attributes to set
   */
  public void setAttributes(Set<Attribute> attributes) {
    this.attributes = attributes;
  }

  /**
   * get createTime.
   * 
   * @return the createTime
   */
  public Date getCreateTime() {
    return new Date(createTime.getTime());
  }

  /**
   * set createTime.
   * 
   * @param createTime the createTime to set
   */
  public void setCreateTime(Date createTime) {
    this.createTime = new Date(createTime.getTime());
  }

  /**
   * get lastModifiedTime.
   * 
   * @return the lastModifiedTime
   */
  public Date getLastModifiedTime() {
    return new Date(lastModifiedTime.getTime());
  }

  /**
   * set lastModifiedTime.
   * 
   * @param lastModifiedTime the lastModifiedTime to set
   */
  public void setLastModifiedTime(Date lastModifiedTime) {
    this.lastModifiedTime = new Date(lastModifiedTime.getTime());
  }

  /**
   * get products.
   * 
   * @return the products
   */
  public Set<Product> getProducts() {
    return products;
  }

  /**
   * set products.
   * 
   * @param products the products to set
   */
  public void setProducts(Set<Product> products) {
    this.products = products;
  }

  /**
   * hashCode.
   */
  @Override
  public int hashCode() {
    return Objects.hash(id, name, description);
  }

  /**
   * equals.
   */
  @Override
  public boolean equals(Object obj) {
    boolean equalsResult = false;
    if (this == obj) {
      equalsResult = true;
    } else if (obj instanceof Template) {
      final Template other = (Template) obj;
      equalsResult = Objects.equals(id, other.id) && Objects.equals(name, other.name)
          && Objects.equals(description, other.description);
    }
    return equalsResult;
  }
}
